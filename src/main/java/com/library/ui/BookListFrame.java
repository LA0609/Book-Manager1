    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // 查询按钮事件
        String searchField = (String) jComboBox1.getSelectedItem(); // 书名/作者/出版社/可借阅/已借阅
        String keyword = jTextField1.getText().trim();

        // 普通模糊查询（书名/作者/出版社）必须输入关键词
        if (!searchField.equals("可借阅") && !searchField.equals("已借阅")) {
            if (keyword.isEmpty()) {// 判断关键词为空
                JOptionPane.showMessageDialog(this, "请输入内容再查询", "提示", JOptionPane.INFORMATION_MESSAGE);
                return; // 终止后续查询逻辑，不再执行数据库查询
            }
        }

        try {
            BookDao bookDao = new BookDao();
            // 调用搜索方法，根据指定字段与关键词查询匹配的图书集合
            List<Book> books = bookDao.search(searchField, keyword);
            // 表头
            String[] columnNames = {"图书ID", "图书编号", "书名", "作者", "出版社", "库存", "总藏书量"};
            DefaultTableModel model = new DefaultTableModel(columnNames, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            // 填充查询结果
            for (Book b : books) {
            model.addRow(new Object[]{
            b.getId(),
            b.getIsbn(),
            b.getName(),
            b.getAuthor(),
            b.getPublisher(),
            b.getCurrentCount(),
            b.getTotalCount()
            });
            }

            tableBooks.setModel(model);

            // 提示查询结果数量
            if (books.isEmpty()) {
                JOptionPane.showMessageDialog(this, "未找到匹配的图书！", "提示", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "查询失败：" + e.getMessage(), "错误", JOptionPane.ERROR_MESSAGE);
             e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

