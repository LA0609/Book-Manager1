    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        int selectedRow = tableBooks.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "请先选择要删除的图书！");
            return;
        }
        int opt = JOptionPane.showConfirmDialog(this, "确定要删除该图书吗？", "删除确认", JOptionPane.YES_NO_OPTION);
        if (opt == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) tableBooks.getModel();
            int id = (Integer) model.getValueAt(selectedRow, 0); // 图书ID在第1列(索引0)
            // 从数据库删除
            new BookDao().delete(id);
            // 刷新表格
            loadBookData();
            JOptionPane.showMessageDialog(this, "列表移除成功！");
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed

