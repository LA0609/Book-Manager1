    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // 获取所有输入内容
        String isbn = jTextField2.getText().trim();      // 图书编号
        String name = jTextField3.getText().trim();       // 书名
        String author = jTextField4.getText().trim();     // 作者
        String publisher = jTextField5.getText().trim();  // 出版社
        String totalStr = jTextField6.getText().trim();   // 库存
        

        // 空值校验
        if (isbn.isEmpty() || name.isEmpty() || author.isEmpty() || publisher.isEmpty()) {
            JOptionPane.showMessageDialog(this, "图书编号、书名、作者、出版社不能为空！", "提示", JOptionPane.WARNING_MESSAGE);
            return;
        }
         BookDao bookDao = new BookDao();
        // 调用Dao方法判断编号是否已存在
       if(bookDao.isIsbnExist(isbn)){
        // 需求指定弹窗文字+标题
        JOptionPane.showMessageDialog(this, "图书编号重复，请重新添加！", "添加失败", JOptionPane.ERROR_MESSAGE);
        return; // 终止执行，不会新增数据
    }

        try {
             // 库存为空则默认0，否则转换为整数
            int totalCount = totalStr.isEmpty() ? 0 : Integer.parseInt(totalStr);
            // 校验库存必须为正整数
            if (totalCount <= 0) {
                JOptionPane.showMessageDialog(this, "库存数量必须大于0！", "输入错误", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 封装图书对象
            Book book = new Book();
            book.setIsbn(isbn);
            book.setName(name);
            book.setAuthor(author);
            book.setPublisher(publisher);
            book.setTotalCount(totalCount);
            //初始库存=总藏书量，解决库存为0问题
            book.setCurrentCount(totalCount);

            // 插入数据库
            new BookDao().add(book);

            // 添加成功弹窗
            JOptionPane.showMessageDialog(this, "添加成功！", "提示", JOptionPane.INFORMATION_MESSAGE);

            // 返回原图书列表并刷新数据
            if (mainFrame != null) {
            mainFrame.loadBookData();  // 只保留刷新
        }
          // 关闭当前添加弹窗   
          this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "库存请输入有效数字！", "输入错误", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "添加失败：" + e.getMessage(), "错误", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

