    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // 1. 获取页面所有输入内容，trim去除首尾空格
        String name = jTextField2.getText().trim();
        String gender = (String) jComboBox1.getSelectedItem();
        String phone = jTextField1.getText().trim();
        String cardNo = jTextField3.getText().trim();

        // 2. 第一层校验：非空判断，姓名、电话、证件号必填
        if(name.isEmpty() || phone.isEmpty() || cardNo.isEmpty()){
            JOptionPane.showMessageDialog(this, "姓名、电话、证件号不能为空！");
            // 校验不通过，终止后续新增逻辑
            return;
        }

        // 3. 第二层校验：调用工具类正则校验格式合法性
        // 校验手机号必须为11位数字
        if(!ValidatorUtil.checkPhone(phone)){
            JOptionPane.showMessageDialog(this, "手机号格式错误！请输入11位有效手机号");
            return;
        }
        // 校验身份证必须为18位合法证件号
        if(!ValidatorUtil.checkCardNo(cardNo)){
            JOptionPane.showMessageDialog(this, "证件号格式错误！请输入18位身份证号");
            return;
        }

        // 4. 第三层校验：数据库查重，防止重复数据入库
        ReaderDao dao = new ReaderDao();
        // 根据手机号查询，手机号唯一不可重复
        Reader samePhone = dao.findByPhone(phone);
        if(samePhone != null){
            JOptionPane.showMessageDialog(this, "该手机号已被其他读者注册，无法重复添加！");
            return;
        }
        // 根据证件号查询，身份证唯一不可重复
        Reader sameCard = dao.findByCardNo(cardNo);
        if(sameCard != null){
            JOptionPane.showMessageDialog(this, "该证件号已存在，无法重复添加！");
            return;
        }

        // 5. 所有校验全部通过，封装Reader实体对象
        Reader reader = new Reader();
        reader.setName(name);         // 赋值姓名
        reader.setGender(gender);     // 赋值性别
        reader.setPhone(phone);       // 赋值手机号
        reader.setCardNo(cardNo);     // 赋值身份证
        reader.setStatus("正常");     // 新增读者默认状态为正常

        try{
            // 调用DAO层新增方法，执行INSERT插入数据库
            dao.add(reader);
        }catch(Exception e){
            // 捕获数据库异常（连接失败、SQL错误等）
            JOptionPane.showMessageDialog(this, "新增失败："+e.getMessage());
            // 控制台打印异常堆栈，方便开发调试排错
            e.printStackTrace();
            return;
        }

        // 6. 新增成功处理逻辑
        JOptionPane.showMessageDialog(this, "新增读者成功！");
        // 判断主窗口引用不为空，调用主窗口刷新表格方法同步最新数据
        if(mainWindow != null){
            mainWindow.loadTableData();
        }
        // 关闭新增弹窗，回到读者管理主界面
        this.dispose();                    
    }//GEN-LAST:event_jButton1ActionPerformed

