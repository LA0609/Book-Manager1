    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();// 未选中行提示
    if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "请先选中要注销的读者！");
            return;
        }

        // 获取选中读者主键ID
        int readerId = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        // 获取读者姓名（可用于确认弹窗拓展展示）
        String name = jTable1.getValueAt(row, 1).toString();
        // 弹出确认对话框，询问用户是否确认注销
        int opt = javax.swing.JOptionPane.showConfirmDialog(this, "确定要注销该读者吗？");
        // 用户点击“是”执行注销逻辑
        if (opt == javax.swing.JOptionPane.YES_OPTION) {
            try{
                // 创建DAO对象执行注销方法（逻辑修改状态，不物理删除）
                ReaderDao dao = new ReaderDao();
                dao.cancelReader(readerId);
                // 注销成功弹窗提示
                javax.swing.JOptionPane.showMessageDialog(this, "注销成功");
                // 刷新表格展示最新数据
                loadTableData();
            }catch(Exception e){
                // 异常捕获，提示失败并打印错误堆栈
                javax.swing.JOptionPane.showMessageDialog(this, "注销失败："+e.getMessage());
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton5ActionPerformed
    /**
     * 表格数据加载通用工具方法
     * 复用点：窗口初始化、刷新按钮、新增/修改/注销操作后都会调用
     * 功能：查询数据库全部读者，清空表格并循环渲染所有读者数据
     */
    void loadTableData() {
        // 获取表格模型
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        // 清空表格原有所有行
        model.setRowCount(0);
        // DAO查询全部读者信息
        List<Reader> list = new ReaderDao().findAll();
        // 循环遍历集合，组装表格行数据
        for(Reader r : list){
            model.addRow(new Object[]{
                r.getId(),       // 读者编号
                r.getName(),     // 姓名
                r.getGender(),   // 性别
                r.getPhone(),    // 电话
                r.getCardNo(),   // 证件号
                r.getStatus()    // 读者状态
            });
        }
    }

