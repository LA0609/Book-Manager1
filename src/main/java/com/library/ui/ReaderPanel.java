    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField1.getText().trim();
        // 校验：关键词为空，提示用户输入内容
    if (keyword.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "请输入查询内容再查询！");
        return;
    }
    // 获取表格模型对象，用于操作表格行数据
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    try {
        // 调用DAO模糊查询，匹配姓名/编号/证件号等字段
        List<Reader> list = new ReaderDao().searchReader(keyword);
        // 判断查询结果为空，弹窗提示无数据
        if (list.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "无匹配读者信息");
            return;
        }
        // 遍历查询到的读者集合，逐行添加到表格
        for (Reader r : list) {
            model.addRow(new Object[]{
                    r.getId(),       // 读者编号
                    r.getName(),     // 姓名
                    r.getGender(),   // 性别
                    r.getPhone(),    // 联系电话
                    r.getCardNo(),   // 身份证证件号
                    r.getStatus()    // 读者状态（正常/注销）
            });
        }
    } catch (Exception e) {
        // 捕获数据库异常，弹窗提示错误信息
        javax.swing.JOptionPane.showMessageDialog(this, "查询失败：" + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

