    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // 1、读取用户修改后页面输入内容，去除首尾空格
        String name = jTextField2.getText().trim();
        String gender = (String) jComboBox1.getSelectedItem();
        String phone = jTextField3.getText().trim();
        String cardNo = jTextField4.getText().trim();
        String status = (String) jComboBox2.getSelectedItem();

        // 特殊业务校验：如果原状态是已注销，现在改为正常，弹出二次确认
        if(targetReader.getStatus().equals("已注销") && status.equals("正常")){
            int choose = JOptionPane.showConfirmDialog(this, "管理员确认：确定恢复该读者？", "提示", JOptionPane.YES_NO_OPTION);
            // 用户选择否，直接终止修改流程
            if(choose != JOptionPane.YES_OPTION){
                return;
            }
        }

        // 2、非空基础校验，必填项不能为空
        if(name.isEmpty() || phone.isEmpty() || cardNo.isEmpty()){
            JOptionPane.showMessageDialog(this, "姓名、电话、证件号不能为空！");
            return;
        }

        // 3、调用工具类校验手机号、身份证格式合法性
        if(!ValidatorUtil.checkPhone(phone)){
            JOptionPane.showMessageDialog(this, "手机号格式错误！请输入11位有效手机号");
            return;
        }
        if(!ValidatorUtil.checkCardNo(cardNo)){
            JOptionPane.showMessageDialog(this, "证件号格式错误！请输入18位身份证号");
            return;
        }

        // 4、查重校验：区分新增，修改时允许保留自身原有手机号/证件号
        ReaderDao dao = new ReaderDao();
        // 手机号查重：存在相同手机号且不是当前编辑读者，则冲突
        Reader samePhone = dao.findByPhone(phone);
        if(samePhone != null && samePhone.getId() != targetReader.getId()){
            JOptionPane.showMessageDialog(this, "该手机号已被其他读者占用！");
            return;
        }
        // 证件号查重：存在相同证件号且不是当前编辑读者，则冲突
        Reader sameCard = dao.findByCardNo(cardNo);
        if(sameCard != null && sameCard.getId() != targetReader.getId()){
            JOptionPane.showMessageDialog(this, "该证件号已被其他读者占用！");
            return;
        }

        // 5、全部校验通过，将新编辑数据覆盖原始读者对象
        targetReader.setName(name);
        targetReader.setGender(gender);
        targetReader.setPhone(phone);
        targetReader.setCardNo(cardNo);
        targetReader.setStatus(status);

        try{
            // 6、调用DAO层updateReader方法执行数据库更新操作
            dao.updateReader(targetReader);
        }catch(Exception e){
            // 捕获数据库异常，弹窗提示错误信息，控制台打印堆栈用于调试
            JOptionPane.showMessageDialog(this, "修改失败："+e.getMessage());
            e.printStackTrace();
            return;
        }

        // 7、修改成功收尾逻辑
        JOptionPane.showMessageDialog(this, "修改成功！");
        // 主窗口存在时刷新表格，同步最新数据库数据
        if(mainPanel != null){
            mainPanel.loadTableData();
        }
        // 关闭修改弹窗，返回读者管理主界面
        this.dispose();                  
    }//GEN-LAST:event_jButton1ActionPerformed

