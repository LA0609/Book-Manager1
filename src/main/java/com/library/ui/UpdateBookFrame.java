    /**
     * 从数据库加载选中图书的数据，填充到输入框
     */
    void loadBookData() {
    if (bookId <= 0) return;
    try {
        BookDao bookDao = new BookDao();
        Book book = bookDao.findById(bookId);
        if (book != null) {
            jTextField3.setText(book.getName());                   // 书名
            jTextField4.setText(book.getAuthor());                 // 作者
            jTextField5.setText(book.getPublisher());              // 出版社
            jTextField6.setText(String.valueOf(book.getTotalCount())); // 库存/总藏书量
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "加载数据失败：" + e.getMessage(), "错误", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (bookId <= 0) {
            JOptionPane.showMessageDialog(this, "未指定要修改的图书！", "错误", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 从输入框获取修改后的数据
        String name = jTextField3.getText().trim();
        String author = jTextField4.getText().trim();
        String publisher = jTextField5.getText().trim();
        String totalCountStr = jTextField6.getText().trim();


        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "书名不能为空！", "提示", JOptionPane.WARNING_MESSAGE);
            return;
           }
try {
    int totalCount = totalCountStr.length() == 0 ? 0 : Integer.parseInt(totalCountStr);
    if (totalCount < 0) {
        JOptionPane.showMessageDialog(this, "总藏书量不能为负数！", "输入错误", JOptionPane.WARNING_MESSAGE);
        return;
    }
    // 计算已借出数量，新总藏不能小于已借出量
    BookDao dao = new BookDao();
    Book oldBook = dao.findById(bookId);
    int borrowedCount = (oldBook != null) ? (oldBook.getTotalCount() - oldBook.getCurrentCount()) : 0;
    if (totalCount < borrowedCount) {
        JOptionPane.showMessageDialog(this,
            "总藏书量不能小于已借出数量（" + borrowedCount + " 本）！\n请先归还部分图书后再修改。",
            "提示", JOptionPane.WARNING_MESSAGE);
        return;
    }
    // 新在馆量 = 新总藏 - 已借出
    int currentCount = totalCount - borrowedCount;

    Book book = new Book();
    book.setId(bookId);
    book.setIsbn(oldBook != null ? oldBook.getIsbn():"");
    book.setName(name);
    book.setAuthor(author);
    book.setPublisher(publisher);
    book.setTotalCount(totalCount);
    book.setCurrentCount(currentCount);

    // 更新到数据库
    BookDao bookDao = new BookDao();
    bookDao.update(book);
    JOptionPane.showMessageDialog(this, "修改成功！");
    this.dispose(); // 关闭当前修改窗口     
    // 刷新原有列表窗口，不要new新窗口        
      if (mainListFrame != null) {
            mainListFrame.loadBookData();
             mainListFrame.toFront(); // 前置主界面，避免隐藏
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "修改失败：" + ex.getMessage(), "错误", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

